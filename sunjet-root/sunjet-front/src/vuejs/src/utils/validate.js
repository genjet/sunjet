/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validUsername(str) {
  // const validmap = ['admin', 'editor', 'user']
  // return validmap.indexOf(str.trim()) >= 0
  return str.trim().length > 2
}